vector<int> mergeSortedArrays(int arr1[], int arr2[], int n, int m)
{

    vector<int> v;
    int i = 0, j = 0;
    while (i < n && j < m)
    {
        if (arr1[i] < arr2[j])
            v.push_back(arr1[i++]);
        else if (arr1[i] > arr2[j])
            v.push_back(arr2[j++]);
    }
    while (i < n)
    {
        v.push_back(arr1[i++]);
    }
    while (j < m)
    {
        v.push_back(arr2[j++]);
    }
    return v;
}

